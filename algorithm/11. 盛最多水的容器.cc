#include <deque>
#include <map>
#include <numeric>
#include <queue>
#include <stack>
#include <string>
#include <unordered_map>
#include <unordered_set>
#include <vector>

class Solution {
 public:
  int maxArea(std::vector<int>& height) {
    int l = 0;
    int r = height.size() - 1;
    int result = 0;
    while (l < r) {
      if (height[l] < height[r]) {
        result = std::max(result, (r - l) * height[l]);
        ++l;
      } else {
        result = std::max(result, (r - l) * height[r]);
        --r;
      }
    }
    return result;
  }
};